// ─── Well Test ────────────────────────────────────────────────────────────────

export function formatWellTest(t: WellTest): string {
  let md = `# Well Test\n\n` +
    field('Date', t.date) +
    field('Lab', t.lab) +
    field('Technician', t.technician) +
    field('Overall Result', t.overallResult) +
    field('Next Test', t.nextTestDate) +
    field('Notes', t.notes)

  if (t.parameters.length > 0) {
    md += '\n## Parameters\n\n| Parameter | Value | Unit | Result |\n|-----------|-------|------|--------|\n'
    for (const p of t.parameters) {
      md += `| ${p.name} | ${p.value} | ${p.unit} | ${p.passFail} |\n`
    }
  }

  return md + footer()
}

export function wellTestFilename(t: WellTest): string {
  return `well_test_${t.date}_${t.id.slice(0, 8)}.md`
}

