const footer = () => `\n---\n*Generated by Property Manager · ${new Date().toISOString()}*\n`

function field(label: string, value: unknown): string {
  if (value == null || value === '') return ''
  return `- **${label}:** ${value}\n`
}

// ─── Maintenance ──────────────────────────────────────────────────────────────

export function formatMaintenanceTask(t: MaintenanceTask): string {
  return `# Maintenance: ${t.title}\n\n` +
    field('System', t.systemLabel) +
    field('Category', t.categoryId) +
    field('Due Date', t.dueDate) +
    field('Priority', t.priority) +
    field('Status', t.status) +
    field('Recurrence', t.recurrence) +
    field('Est. Cost', t.estimatedCost != null ? `$${t.estimatedCost}` : null) +
    field('Contractor', t.contractor) +
    field('Source', t.source) +
    field('Notes', t.notes) +
    footer()
}

export function taskFilename(t: MaintenanceTask): string {
  const safe = t.title.replace(/[^a-zA-Z0-9]/g, '_').replace(/__+/g, '_').slice(0, 40)
  return `task_${safe}_${t.id.slice(0, 8)}.md`
}

